def addition(num1, num2)
  value = num1 + num2
  value
end

def subtraction(num1, num2)
  value = num1 - num2
  value
end

def division(num1, num2)
  value = num1 / num2
  value
end

def multiplication(num1, num2)
  value = num1 * num2
  value
end

def modulo(num1, num2)
    value = num1 % num2
    value
end

def square_root(num)
  value = Math.sqrt(num)
  value
end

def order_of_operation(num1, num2, num3, num4)
  value = num1 + (( num2 * num3 ) / num4 )
  value
end
